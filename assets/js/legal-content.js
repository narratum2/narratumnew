// Cookie preference handler
window.saveCookiePreferences = function() {
    const analyticsCookies = document.getElementById('analyticsCookies').checked;
    const preferenceCookies = document.getElementById('preferenceCookies').checked;
    
    // Save preferences
    localStorage.setItem('cookiePreferences', JSON.stringify({
        analytics: analyticsCookies,
        preferences: preferenceCookies
    }));
    
    // Update Google Analytics consent
    if (typeof gtag !== 'undefined') {
        gtag('consent', 'update', {
            'analytics_storage': analyticsCookies ? 'granted' : 'denied'
        });
    }
    
    // Show confirmation
    const button = document.querySelector('.save-preferences');
    const originalText = button.textContent;
    button.textContent = 'Preferences Saved!';
    button.style.backgroundColor = 'var(--accent-gold)';
    button.style.color = 'var(--bg-primary)';
    
    setTimeout(() => {
        button.textContent = originalText;
        button.style.backgroundColor = '';
        button.style.color = '';
    }, 2000);
    
    // Close modal after short delay
    setTimeout(() => {
        if (window.closeLegalModal) {
            window.closeLegalModal();
        }
    }, 2500);
};